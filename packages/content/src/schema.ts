export const EffectDefSchema = z.object({
  on: GameEventSchema,
  do: EffectFactoryIdSchema,
  params: z.record(z.unknown()).optional(),
}).strict();

// `.strict()`: a typo'd optional key (`goldentext`, `targetTribes`) would otherwise validate silently and
// the card would ship without the feature. Every legit field is declared below, so this only rejects typos.
export const CardDefSchema = z.object({
  id: z.string().min(1),
  name: z.string().min(1),
  tribe: TribeSchema,
  tribe2: TribeSchema.optional(),
  tier: z.union([
    z.literal(1),
    z.literal(2),
    z.literal(3),
    z.literal(4),
    z.literal(5),
    z.literal(6),
  ]),
  attack: z.number().int().nonnegative(),
  health: z.number().int().positive(),
  keywords: z.array(KeywordSchema),
  effects: z.array(EffectDefSchema),
  // Body text is mechanical-only and may be empty for keyword-only cards
  // (the keyword badge + hover tooltip carry the meaning).
  text: z.string(),
  goldenText: z.string().optional(),
  universalTribe: z.boolean().optional(),
  imp: z.boolean().optional(),
  token: z.boolean().optional(),
  ascendAt: z.number().int().positive().optional(),
  ascendInto: z.string().optional(),
  attackOnSummon: z.boolean().optional(),
  spell: z.boolean().optional(),
  singleCast: z.boolean().optional(),
  cost: z.number().int().nonnegative().optional(),
  target: z.enum(['friendly', 'any']).optional(),
  targetTribe: TribeSchema.optional(),
  targetMaxTier: z.number().int().positive().optional(),
  targetNoGolden: z.boolean().optional(),
  fodderMult: z.number().int().positive().optional(),
  manaPerTurn: z.number().int().positive().optional(),
  rallyMechAtk: z.number().int().positive().optional(),
  spellAura: z.number().int().positive().optional(),
  fodderAura: z.object({ attack: z.number().int().nonnegative(), health: z.number().int().nonnegative() }).strict().optional(),
  chooseOne: z
    .array(z.object({ text: z.string(), effects: z.array(EffectDefSchema) }).strict())
    .min(2)
    .optional(),
  discoverOnPlay: z
    .object({
      exactTier: z.number().int().positive().optional(),
      tierOffset: z.number().int().optional(),
      filter: z.enum(['battlecry', 'deathrattle']).optional(),
      tribe: z.union([TribeSchema, z.literal('dominant')]).optional(),
      topTierFirst: z.boolean().optional(),
    })
    .strict()
    .optional(),
}).strict();

// ── Quests ───────────────────────────────────────────────────────────────────────────────────────────────
// Kept in lockstep with the `QuestDef` type in @game/core (same as CardDefSchema). `.strict()` rejects typo'd
// keys so a malformed test quest surfaces in `npm test`, not at runtime.
export const QuestTierSchema = z.enum(['lesser', 'greater', 'capstone']);
export const QuestObjectiveEventSchema = z.enum(['buy', 'play', 'sell', 'roll', 'summon', 'shout']);

// The reward palette — a discriminated union kept in lockstep with the `QuestReward` type in @game/core.
export const QuestRewardSchema = z.discriminatedUnion('kind', [
  z.object({ kind: z.literal('buffBoard'), attack: z.number().int().nonnegative(), health: z.number().int().nonnegative() }).strict(),
  z.object({
    kind: z.literal('grant'),
    randomTribe: TribeSchema.optional(),
    randomCount: z.number().int().positive().optional(),
    cards: z.array(z.string().min(1)).optional(),
    repeatInTurns: z.number().int().positive().optional(),
  }).strict(),
  z.object({ kind: z.literal('shoutDouble'), count: z.number().int().positive() }).strict(),
]);

export const QuestDefSchema = z.object({
  id: z.string().min(1),
  name: z.string().min(1),
  tribe: TribeSchema,
  tier: QuestTierSchema,
  objective: z.object({
    event: QuestObjectiveEventSchema,
    count: z.number().int().positive(),
    tribe: TribeSchema.optional(),
  }).strict(),
  reward: QuestRewardSchema,
}).strict();
